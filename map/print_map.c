#include "../so_long.h"

void	print_map(char **map)
{
	int i;
	int j;

	i = 0;
	ft_printf("[print_map]\n");
	while (map[i])
	{
		j = 0;
		while (map[i][j])
		{
			write(1, &map[i][j], 1);
			j++;
		}
		write(1, "\n", 1);
		i++;
	}
	write(1, "\n", 1);
}
